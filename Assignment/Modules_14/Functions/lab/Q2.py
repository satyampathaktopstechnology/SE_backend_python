# Write a Python program to create a calculator using functions. 
def add(a,b):
    return a+b

def sub(a,b):
    return a-b

def mul(a,b):
    return a*b

def divsion(a,b):
    return a/b

print("Addtion : ",add(10,20))
print("Subtraction : ",sub(50,25))
print("Multiplication : ",mul(45,2))
print("Divison : ",divsion(10,2))
