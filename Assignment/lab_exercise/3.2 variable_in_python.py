# Program to demonstrate variables and data types

# String type variable
student_name = "Satyam"

# Integer type variable
student_age = 20

# Float type variable
student_height = 5.8

# Boolean type variable
is_student = True

# List type variable
subjects = ["Python", "C", "SQL"]

# Printing variables
print("Student Name:", student_name)
print("Student Age:", student_age)
print("Student Height:", student_height)
print("Is Student:", is_student)
print("Subjects:", subjects)

# Showing data types using type()
print("\nData Types:")
print("Type of student_name:", type(student_name))
print("Type of student_age:", type(student_age))
print("Type of student_height:", type(student_height))
print("Type of is_student:", type(is_student))
print("Type of subjects:", type(subjects))
