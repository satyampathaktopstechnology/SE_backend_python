# Integer
age = 22

# Float
percentage = 78.5

# String
name = "Chetan Patil"

# Boolean
is_student = True

# List
subjects = ["C", "C++", "HTML", "SQL"]

# Tuple
marks = (80, 75, 90)

# Set
cities = {"Surat", "Mumbai", "Pune"}

# Dictionary
student = {
    "name": "Chetan",
    "age": 20,
    "course": "BCA"
}

# Printing values and their data types
print(age, type(age))
print(percentage, type(percentage))
print(name, type(name))
print(is_student, type(is_student))
print(subjects, type(subjects))
print(marks, type(marks))
print(cities, type(cities))
print(student, type(student))
